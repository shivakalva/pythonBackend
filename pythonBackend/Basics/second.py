######### In python we can give the heterogenous data types in the list #########
a = [1, 2, "shiva", "hanuman", 2, 1]
print(a)
print(a[0])
print(a[-1])
print(a[1:3])
a.insert(6, "shi")
print(a)
###########  ADDING THE LETTERS ######################
a.append("shivaji")
print(a)
###########  ASSIGINING THE LETTERS ######################
a[2] = "jai"
print(a)
################### DELETE THE ZERO INDEX ELEMENT #####################
del a[0]
print(a)
